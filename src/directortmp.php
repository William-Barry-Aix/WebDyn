				<h2>Biography</h2>
				<p>Né à Guadalajara, Guillermo del Toro est élevé par sa grand-mère catholique. C'est à 8 ans qu'il est pour la première fois impliqué dans la production cinématographique[réf. nécessaire]. Il étudie à l'Instituto de Ciencias avant de devenir l'élève de Dick Smith, créateur des maquillages de Little Big Man et L'Exorciste notamment. Il travaille comme maquilleur et spécialiste en effets spéciaux pendant environ dix ans, et crée au début des années 1980 sa propre société, baptisée Necropia2. Il produit son premier film en 1985, à l'âge de 21 ans, et cofonde le festival international du film de Guadalajara, dont la première édition a lieu en 1986.</p>

				<p>En 1993, il sort son premier long-métrage comme réalisateur, Cronos, un film d'horreur avec Ron Perlman qui remporte de nombreux prix, notamment au Festival de Cannes 1993 et aux Premio Ariel (équivalents des César du cinéma au Mexique)2. Il fonde sa propre société de production : le Tequila Gang.</p>

				<p>En 1997, son père, Federico, est enlevé au Mexique puis libéré contre une rançon, ce qui incite le cinéaste à s'expatrier3.</p>
